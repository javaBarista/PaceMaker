package com.example.pacemaker.ui.word;

public class DayForm {
    private String day1;
    private String day2;
    private String day3;

    public DayForm(String day1, String day2, String day3){
        this.day1 = day1;
        this.day2 = day2;
        this.day3 = day3;
    }

    public String getDay1() { return day1; }
    public String getDay2() { return day2; }
    public String getDay3() { return day3; }
}
